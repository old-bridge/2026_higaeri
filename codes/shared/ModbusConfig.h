#ifndef MODBUS_CONFIG_H
#define MODBUS_CONFIG_H

// ===================================================================
// ボーレート設定
// ===================================================================
const long BAUDRATES[] = {9600, 38400, 115200, 921600, 5000000};
const int NUM_BAUDS = 5;

// ===================================================================
// Slave ID定義
// ===================================================================
#define SLAVE_ID_AIR_DATA   1  // エアデータマイコン
#define SLAVE_ID_DISPLAY    2  // 表示マイコン

// ===================================================================
// DE PIN定義（各マイコン固有）
// 実装時は各INOファイルで確実に設定すること
// ===================================================================
#ifndef DE_PIN
  #error "DE_PIN must be defined in the .ino file before including ModbusMaster.h or ModbusSlave.h"
#endif

// ===================================================================
// レジスタアドレス定義
// ===================================================================

// ========== エアデータマイコン用レジスタ ==========
// 読み込み用（Slave用）
#define AIR_REG_READ        0    // アドレス 0～3：センサー値
#define AIR_REG_READ_SIZE   4    // 回転数、AS5600①、AS5600②、バッテリー電圧

// 書き込み用（マスター→スレーブへの制御）
#define AIR_REG_WRITE       10   // アドレス 10：LED制御等
#define AIR_REG_WRITE_SIZE  1

// 制御用
#define AIR_REG_BAUD_CTRL   300  // ボーレート切り替え指令

// ========== 表示マイコン用レジスタ ==========
// 読み込み用（Slave用）
#define DISP_REG_READ       0    // アドレス 0～4：センサー値
#define DISP_REG_READ_SIZE  5    // ポテンション①②、バッテリー、超音波高度、気圧高度

// 書き込み用（マスター→スレーブへの制御）
#define DISP_REG_WRITE      10   // アドレス 10：LED制御等
#define DISP_REG_WRITE_SIZE 1

// 制御用
#define DISP_REG_BAUD_CTRL  300  // ボーレート切り替え指令

// ===================================================================
// レジスタ値の意味（エアデータマイコン）
// ===================================================================
// AIR_REG_READ + 0 : ロータリーエンコーダの回転数
// AIR_REG_READ + 1 : AS5600①の角度
// AIR_REG_READ + 2 : AS5600②の角度
// AIR_REG_READ + 3 : バッテリー電圧（ADC値）

// ===================================================================
// レジスタ値の意味（表示マイコン）
// ===================================================================
// DISP_REG_READ + 0 : ポテンショメータ①の角度
// DISP_REG_READ + 1 : ポテンショメータ②の角度
// DISP_REG_READ + 2 : バッテリー電圧（ADC値）
// DISP_REG_READ + 3 : 超音波センサの高度
// DISP_REG_READ + 4 : 大気圧センサの高度

// ===================================================================
// LED制御用の値
// ===================================================================
#define LED_OFF  0
#define LED_ON   1

// ===================================================================
// タイムアウト設定
// ===================================================================
const unsigned long MODBUS_TIMEOUT = 1000;  // ms

#endif  // MODBUS_CONFIG_H
